## Create functions to cache the inverse of a matrix
## Matrix supplied should be invertible

## First function to create a special matrix

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinverse <- function(inverse) m <<- inverse
        getinverse <- function() m
        list(set = set, get = get,
             setinverse = setinverse,
             getinverse = getinverse)  
}


## Compute inverse of special matrix returned by the above function. 
## The function first checks if the inverse has already been calculated and if it has, gets the inverse from cache.
## Otherwise the inverse is calculated and value is set in the cache

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        m <- x$getinverse()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setinverse(m)
        m
}